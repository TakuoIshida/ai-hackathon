type Database = typeof DbClient;

const INVITE_TTL_DAYS = 7;
const INVITE_TTL_MS = INVITE_TTL_DAYS * 24 * 60 * 60_000;

// ---------------------------------------------------------------------------
// createInvitation
// ---------------------------------------------------------------------------

export type CreateInvitationInput = {
  email: string;
  /**
   * The requested role for the invitee. Persisted on tenant.invitations.role
   * (ISH-252) and read back when the invitation is accepted, so an "owner"
   * invite actually creates an owner tenant_members row.
   */
  role: TenantMemberRole;
};

export type CreateInvitationResult =
  | { kind: "ok"; invitationId: string; token: string; expiresAt: Date }
  | { kind: "already_member" }
  | { kind: "already_invited" };

/**
 * ISH-293: dependencies needed to actually deliver the invitation email
 * after the row is inserted. The route layer wires the production Resend
 * sender (or a no-op when RESEND_API_KEY is missing); tests inject a fake.
 *
 * Email delivery is best-effort by design — see the comment on the send
 * call below for the rationale.
 */
export type CreateInvitationDeps = {
  sendEmail: SendEmailFn;
  appBaseUrl: string;
};

/**
 * Issue a new tenant-scoped invitation.
 *
 * Pre-conditions (checked by the route before calling):
 *   - caller is authenticated and is a member of the tenant with role="owner"
 *
 * This function handles:
 *   - email already in tenant_members (via common.users join) → already_member
 *   - open invitation already exists → already_invited (DB partial unique
 *     also enforces this, but we return a friendly domain result)
 *   - INSERT tenant.invitations (token generated by defaultRandom)
 *   - dispatch the workspace invite email via `deps.sendEmail` (ISH-293).
 *     Best-effort: a delivery failure is logged + swallowed so the row stays
 *     committed (the operator can hit 再送 from the listing). Mirrors the
 *     same policy as `resendTenantInvitation` and the legacy
 *     `workspaces/usecase::issueInvitation`.
 */
export async function createInvitation(
  database: Database,
  tenantId: string,
  inviterUserId: string,
  input: CreateInvitationInput,
  deps: CreateInvitationDeps,
): Promise<CreateInvitationResult> {
  // ISH-195: normalize email to lowercase BEFORE every read/write so the
  // uniqueness checks, the partial unique index `uniq_tenant_email_open`,
  // and the future acceptance comparison all see one canonical form.
  // Previously: `Bob@x.com` invite + `bob@x.com` sign-up bypassed the
  // already_invited / already_member checks because they string-compared
  // the raw values.
  const email = input.email.toLowerCase();

  // Check if a user with this email is already a tenant member.
  const [existingMember] = await database
    .select({ id: tenantMembers.id })
    .from(tenantMembers)
    .innerJoin(users, eq(tenantMembers.userId, users.id))
    .where(eq(users.email, email))
    .limit(1);

  if (existingMember) {
    return { kind: "already_member" };
  }

  // Check for an open invitation for this email in this tenant.
  const existing = await findOpenInvitationByEmail(database, tenantId, email);
  if (existing) {
    return { kind: "already_invited" };
  }

  const expiresAt = new Date(Date.now() + INVITE_TTL_MS);
  const invitation = await insertInvitation(database, {
    tenantId,
    email,
    invitedByUserId: inviterUserId,
    expiresAt,
    role: input.role,
  });

  // ISH-293: dispatch the invite email. Best-effort — the row is already
  // committed, so a delivery failure must NOT roll it back (operator can
  // re-send from the listing). We surface the error via console.warn so a
  // silently-misconfigured sender is visible in the logs rather than a
  // ghost-success at the UI.
  try {
    const [workspace, inviter] = await Promise.all([
      findWorkspaceById(database, tenantId),
      findUserById(database, inviterUserId),
    ]);
    await deps.sendEmail(
      workspaceInviteEmail({
        to: email,
        workspaceName: workspace?.name ?? "ワークスペース",
        acceptUrl: `${deps.appBaseUrl}/invite/${invitation.token}`,
        expiresAt,
        inviterName: inviter?.name ?? null,
      }),
    );
  } catch (err) {
    console.warn("[invite] failed to send invitation email; row kept:", err);
  }

  return { kind: "ok", invitationId: invitation.id, token: invitation.token, expiresAt };
}

